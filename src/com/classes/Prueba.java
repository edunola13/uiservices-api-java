package com.classes;

public class Prueba {
	private String hola= "asad";
	private String chau= "aasasa";
	
	public Prueba(){
	}

	public String getHola() {
		return hola;
	}

	public void setHola(String hola) {
		this.hola = hola;
	}

	public Integer getChau() {
		return Integer.parseInt(this.chau);
	}

	public void setChau(String chau) {
		this.chau = chau;
	}
	
	
}
